package com.ericdaugherty.mail.server.info;

/**
 * This interface defines what is considered to be unique (as a means of
 * identifying a given instance) for the purpose of persisting classes
 * that implement it.
 * 
 * One such case is the implementing classes of the {@link User} The purpose of
 * this interface is to define the boundaries between what is admissible in the
 * SMTP/DNS domains and what is accomplishable when attempting to create a folder
 * under which to store a given user's messages. While POSIX File Systems are
 * case-sensitive, the Windows FS is not (in the W32 namespace). The solution
 * used does restrict the breadth of usable account names but places the least
 * possible burden on the server at any given time while allowing the folder name
 * to be easily AND uniquely identifiable.
 * 
 * 
 * @author Andreas Kyrmegalos
 */
public interface Persistable {
   
   String getUniqueName();
}
